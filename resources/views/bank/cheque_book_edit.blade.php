@section('js')

    <script>
        $(document).ready(function(){

        });

        $(document).on("submit", "#cheque_book_form", function(event) {
            event.preventDefault();
            show_loader();

            var bank_account_id = $("#bank_account_id").val();
            var book_number = $("#book_number").val();

            var validate = "";

            if (bank_account_id.trim() == "") {
                validate = validate + "Bank Account is required</br>";
            }
            if (book_number.trim() == "") {
                validate = validate + "Book Number is required</br>";
            }

            if (validate == "") {
                var formData = new FormData($("#cheque_book_form")[0]);
                var url = "{{ url('cheque_books/update') }}";

                $.ajax({
                    type: "POST",
                    url: url,
                    data: formData,
                    success: function(data) {
                        hide_loader();
                        if (data.status == 200) {
                            $("#success_message").show();
                            $("#error_message").hide();
                            $("#success_message").html(data.reason);
                            setTimeout(function(){
                                $("#success_message").hide();
                            },1000)
                        } else {
                            $("#success_message").hide();
                            $("#error_message").show();
                            $("#error_message").html(data.reason);
                        }
                    },
                    error: function(data) {
                        hide_loader();
                        $("#success_message").hide();
                        $("#error_message").show();
                        $("#error_message").html(data);
                    },
                    cache: false,
                    contentType: false,
                    processData: false
                });
            } else {
                hide_loader();
                $("#success_message").hide();
                $("#error_message").show();
                $("#error_message").html(validate);
            }
        });
    </script>
@endsection

